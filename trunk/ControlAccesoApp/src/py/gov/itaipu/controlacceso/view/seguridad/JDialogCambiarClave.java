    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextFieldUsuario.getText() == null || jTextFieldUsuario.getText().equals("")
                    || jPasswordFieldViejo.getText() == null || jPasswordFieldViejo.getText().equals("")
                    || jPasswordFieldNuevo.getText() == null || jPasswordFieldNuevo.getText().equals("")
                    || jPasswordFieldConfirmar.getText() == null || jPasswordFieldConfirmar.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Usuario y clave son requeridos para el cambio de clave.", "Error", 0);
                return;
            }
            AutenticadorAction autenticador = new AutenticadorAction();
            if (autenticador.autenticar(jTextFieldUsuario.getText(), jPasswordFieldViejo.getText())) {
                if (jPasswordFieldNuevo.getText().equals(jPasswordFieldConfirmar.getText())) {
                    List<Usuario> us = usuarioAction.findEqualName(jTextFieldUsuario.getText());
                    if (us.size() < 1) {
                        JOptionPane.showMessageDialog(this, "El usuario no existe, verifique por favor su nombre de usuario", "Error", 0);
                        return;
                    } else {
                        Usuario u = us.get(0);
                        u.setPassword(AutenticadorAction.getDigest(jPasswordFieldNuevo.getText()));
                        usuarioAction.setEntity(u);
                        usuarioAction.guardar();
                        JOptionPane.showMessageDialog(this, "Su clave a sido cambiada con éxito", "Info", 1);
                        this.dispose();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "La nueva clave y la confirmación no coincide, verifique por favor.", "Warning", 2);
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Su clave actual no corresponde.", "Error", 0);
                return;
            }
        } catch (EntidadExiste ex) {
            Logger.getLogger(JDialogCambiarClave.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ErrorInesperado ei) {
            JOptionPane.showMessageDialog(null, "Verfique con el administrador la conexión a la base de datos y vuelva a intentar.", "Error", JOptionPane.ERROR_MESSAGE);
            System.exit(-1);
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

